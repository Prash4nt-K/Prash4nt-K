    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if (validateInput() == false) {
            JOptionPane.showMessageDialog(null, "Userid or Password cannot be left empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String userType = getUserType();
        if (userType == null) {
            JOptionPane.showMessageDialog(null, "Please choose user type!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            User u = new User(userid, password, userType);
            String userName = UserDao.vadlidateUser(u);
            if (userName == null) {
                JOptionPane.showMessageDialog(null, "User Not Found", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                JOptionPane.showMessageDialog(null, "Login successful", "Welcome " + userName, JOptionPane.INFORMATION_MESSAGE);
                UserProfile.setUsername(userName);
                UserProfile.setUsertype(userType);
            }
            if (userType.equalsIgnoreCase("admin")) {
                AdminOptionFrame aof = new AdminOptionFrame();
                aof.setVisible(true);
            } else if (userType.equalsIgnoreCase("doctor")) {
                DoctorScreenFrame vaf = new DoctorScreenFrame();
                vaf.setVisible(true);
                String docid = DoctorDao.retutnDocidWhenDoctorLoginInLoginFrame(txtUserId.getText());
                AppointmentPojo.setDocid(docid);
            } else if (userType.equalsIgnoreCase("Receptiontist")) {
                PatientOptionFrame pof = new PatientOptionFrame();
                pof.setVisible(true);
            }
            this.dispose();

        } catch (Exception ex) {
            System.out.println(ex);
    }//GEN-LAST:event_jButton1ActionPerformed
    }
